def add a, b
    a + b
end

def subtract a, b
    a - b
end

def sum array
    sum = 0

    array.each do |num| sum += num end
    sum
end

def multiply a, b
    a * b
end

def multiplyMany array

    product = 1

    array.each do |num| product *= num end

    product
end

def power a, b
    a ** b
end